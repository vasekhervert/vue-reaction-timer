<template>
  <p>Reaction time: {{ score }} ms</p>
  <p class="rank">Rank: {{rank}}</p>
</template>

<script>
export default {
    props: ['score'],
    data() {
        return {
            rank: null
        }
    },
    mounted() {
        if (this.score < 250) {
            this.rank = 'Superb'
        } else if (this.score < 400) {
            this.rank = 'Rapidfire'
        } else {
            this.rank = 'Snail pace..'
        }
    }
}
</script>

<style>
 .rank {
     font-size: 1.4em;
     color: #0faf87;
     font-weight: 700;
 }
</style>